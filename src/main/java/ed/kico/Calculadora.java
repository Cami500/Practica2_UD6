public class Calculadora {
	
	public int suma(int a, int b) {
		return a + b;
	}
	
	public int resta(int a, int b) {
		return a - b;
	}
	
	public int multiplica(int a, int b){
		return a*b;
	}
	
	public int diveix(int a, int b) {
		return a/b;
	}

}

